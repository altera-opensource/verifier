package com.intel.bkp.crypto.x509.utils;

import com.intel.bkp.crypto.CryptoUtils;
import org.bouncycastle.asn1.x509.AuthorityKeyIdentifier;
import org.bouncycastle.asn1.x509.Extension;
import org.bouncycastle.asn1.x509.SubjectKeyIdentifier;
import org.bouncycastle.asn1.x509.SubjectPublicKeyInfo;
import org.bouncycastle.cert.jcajce.JcaX509ExtensionUtils;

import java.security.NoSuchAlgorithmException;
import java.security.PublicKey;
import java.security.cert.CertificateEncodingException;
import java.security.cert.X509Certificate;

import static com.intel.bkp.crypto.x509.utils.X509ExtensionUtils.getExtensionBytes;

public class KeyIdentifierUtils {

    /**
     * Creates AuthorityKeyIdentifier based on provided certificate.
     * If certificate contains SubjectKeyIdentifier extension, its value is used,
     * if not, identifier is calculated based on SubjectPublicKeyInfo.
     *
     * @throws NoSuchAlgorithmException     - hardly likely to be thrown from BouncyCastle
     * @throws CertificateEncodingException - might be thrown from BouncyCastle if certificate format is invalid
     */
    public static AuthorityKeyIdentifier createAuthorityKeyIdentifier(final X509Certificate certificate)
            throws NoSuchAlgorithmException, CertificateEncodingException {

        return new AuthorityKeyIdentifier(new JcaX509ExtensionUtils()
                .createAuthorityKeyIdentifier(certificate)
                .getKeyIdentifier()
        );
    }

    /**
     * Gets bytes of key identifier from AuthorityKeyIdentifier extension or null, if certificate doesn't contain it.
     */
    public static byte[] getAuthorityKeyIdentifier(final X509Certificate certificate) {
        return getExtensionBytes(certificate, Extension.authorityKeyIdentifier)
                .map(AuthorityKeyIdentifier::getInstance)
                .map(AuthorityKeyIdentifier::getKeyIdentifier)
                .orElse(null);
    }

    /**
     * Creates SubjectKeyIdentifier for public key and calculated using method 2 defined in RFC7093.
     */
    public static SubjectKeyIdentifier createSubjectKeyIdentifier(final PublicKey key) {
        final byte[] ski = calculateSubjectKeyIdentifierUsingMethod2FromRfc7093(key);
        return new SubjectKeyIdentifier(ski);
    }

    /**
     * Calculates SubjectKeyIdentifier for public key, using method 2 defined in RFC7093:
     * "The keyIdentifier is composed of the leftmost 160-bits of the
     * SHA-384 hash of the value of the BIT STRING subjectPublicKey
     * (excluding the tag, length, and number of unused bits)."
     */
    public static byte[] calculateSubjectKeyIdentifierUsingMethod2FromRfc7093(final PublicKey publicKey) {
        final var subjectPublicKeyInfo = SubjectPublicKeyInfo.getInstance(publicKey.getEncoded());
        return CryptoUtils.get20MSBytesForSha384(subjectPublicKeyInfo.getPublicKeyData().getBytes());
    }

    /**
     * Gets bytes of key identifier from SubjectKeyIdentifier extension or null, if certificate doesn't contain it.
     */
    public static byte[] getSubjectKeyIdentifier(final X509Certificate certificate) {
        return getExtensionBytes(certificate, Extension.subjectKeyIdentifier)
                .map(SubjectKeyIdentifier::getInstance)
                .map(SubjectKeyIdentifier::getKeyIdentifier)
                .orElse(null);
    }
}
